                    entry.target.classList.add('visible');
                    revealObserver.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1 });

        document.querySelectorAll('.reveal').forEach(function(el) {
            revealObserver.observe(el);
        });

        /* --- COUNTER ANIMATION (requestAnimationFrame) --- */
        function animateCounter(el, target, duration) {
            var startTime = null;
            function step(timestamp) {
                if (!startTime) startTime = timestamp;
                var progress = Math.min((timestamp - startTime) / duration, 1);
                var eased = 1 - Math.pow(1 - progress, 3);
                el.textContent = Math.floor(eased * target).toLocaleString('en-IN');
                if (progress < 1) requestAnimationFrame(step);
                else el.textContent = target.toLocaleString('en-IN');
            }
            requestAnimationFrame(step);
        }

        var statsDone = false;
        var statsObserver = new IntersectionObserver(function(entries) {
            entries.forEach(function(entry) {
                if (entry.isIntersecting && !statsDone) {
                    statsDone = true;
                    document.querySelectorAll('.count-num').forEach(function(el) {
                        animateCounter(el, parseInt(el.dataset.count), 1600);
                    });
                    statsObserver.disconnect();
                }
            });
        }, { threshold: 0.4 });

        var statsStrip = document.querySelector('.stats-strip');
        if (statsStrip) statsObserver.observe(statsStrip);
    </script>
