    <script>
        function isValidEmail(email) {
            return /^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/.test(String(email).trim());
        }
        function isValidPhone(phone) {
            return String(phone).replace(/\D/g, "").length >= 7;
        }
        function fail(msg, el) {
            alert(msg);
            if (el) el.focus();
            return false;
        }

        $(document).ready(function () {
            $("#contactForm").on("submit", function (e) {
                e.preventDefault();

                const name    = $("#c_name").val().trim();
                const email   = $("#c_email").val().trim();
                const phone   = $("#c_phone").val().trim();
                const message = $("#c_message").val().trim();
                const consent = $("#c_consent").is(":checked");

                if (!name)               return fail("Please enter your full name.", $("#c_name")[0]);
                if (!email)              return fail("Please enter your email address.", $("#c_email")[0]);
                if (!isValidEmail(email))return fail("Please enter a valid email address.", $("#c_email")[0]);
                if (!phone)              return fail("Please enter your mobile number.", $("#c_phone")[0]);
                if (!isValidPhone(phone))return fail("Please enter a valid mobile number.", $("#c_phone")[0]);
                if (!message)            return fail("Please enter your message.", $("#c_message")[0]);
                if (!consent)            return fail("Please accept the privacy policy to continue.", $("#c_consent")[0]);

                const payload = {
                    formtype: "contact_us",
                    name:     name,
                    email:    email,
                    phone:    phone,
                    message:  message
                };

                const $btn = $("#contactForm button[type='submit']");
                $btn.prop("disabled", true).text("Sending...");

                $.ajax({
                    url:  "https://egnindia.com/postformdata.php",
                    type: "POST",
                    data: $.param(payload),
                    success: function (data) {
                        const resp = String(data).trim().toLowerCase();
                        if (resp === "ok") {
                            $("#contactForm")[0].reset();
                            $("#pop_enq_success").show();
                            $("#pop_enq_fail").hide();
                        } else {
                            $("#pop_enq_fail").show();
                        }
                    },
                    error: function () {
                        $("#pop_enq_fail").show();
                    },
                    complete: function () {
                        $btn.prop("disabled", false).text("Send Message →");
                    }
                });
            });
        });
    </script>

