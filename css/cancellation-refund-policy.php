            </p>
            <ul>
                <li>Goods and Services Tax (GST) is applicable on all taxable services at the prevailing statutory rate. GST is charged in addition to the base booking fee unless otherwise stated.</li>
                <li>Payments are accepted via bank transfer, cheque, demand draft, or approved online payment gateways as specified in the booking confirmation.</li>
                <li>Bookings are confirmed only upon receipt of full payment. Partial payments do not constitute a confirmed booking.</li>
                <li>GST amounts, once invoiced, are non-refundable in cash. In cases where a refund is approved, GST components may be adjusted by way of a credit note, subject to applicable GST regulations.</li>
                <li>EGN ConnectX will issue a valid GST invoice for all paid bookings upon confirmation of payment.</li>
                <li>Payment receipts and tax invoices must be retained by participants for their own accounting and audit purposes.</li>
            </ul>
            <p>
                For payment-related queries, please contact <a href="mailto:enquiry@egnindia.com">enquiry@egnindia.com</a> or call our sponsorship desk at <a href="tel:+919639364805">+91 96393 64805</a>.
