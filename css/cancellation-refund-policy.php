        <article class="policy-section" aria-labelledby="crp-intro">
            <h2 id="crp-intro">Introduction</h2>
            <p>
                EGN EDU India Private Limited ("EGN ConnectX", "we", "our", or "us") is committed to maintaining transparent and fair commercial practices for all participants of EGN Connect X 2026, scheduled for <strong>3–4 November 2026</strong> at <strong>NESCO, Mumbai, Maharashtra, India</strong>.
            </p>
            <p>
                This Cancellation &amp; Refund Policy ("Policy") outlines the terms governing cancellations, refund eligibility, and processing timelines for delegate registrations, exhibition stall bookings, and sponsorship packages. This Policy applies to all individuals, organisations, and entities that make a payment to EGN ConnectX for participation in the event.
            </p>
            <div class="policy-note" role="note">
                By completing a paid booking or registration with EGN ConnectX, you acknowledge that you have read, understood, and agree to be bound by this Cancellation &amp; Refund Policy. We recommend reviewing this Policy carefully before confirming any booking.
            </div>
        </article>

        <!-- 2. Delegate Registration Cancellation Policy -->
        <article class="policy-section" aria-labelledby="crp-delegate">
            <h2 id="crp-delegate">Delegate Registration Cancellation Policy</h2>
            <p>
                Paid delegate registrations for conferences, workshops, or leadership sessions at EGN Connect X 2026 are subject to the following cancellation terms. All cancellation requests must be submitted in writing to <a href="mailto:enquiry@egnindia.com">enquiry@egnindia.com</a> and will be acknowledged within 3 business days.
            </p>
            <ul>
                <li><strong>More than 90 days before the event:</strong> 70% refund of the total amount paid. An administrative processing fee of 30% is retained by EGN ConnectX.</li>
                <li><strong>Between 60 and 90 days before the event:</strong> 50% refund of the total amount paid.</li>
                <li><strong>Between 30 and 60 days before the event:</strong> 25% refund of the total amount paid.</li>
                <li><strong>Less than 30 days before the event:</strong> No cash refund will be issued. At the sole discretion of EGN ConnectX, a credit note valid for the next edition of EGN ConnectX may be offered.</li>
                <li><strong>No-show (failure to attend without prior cancellation):</strong> No refund or credit note will be issued under any circumstances.</li>
            </ul>
            <p>
                Free visitor registrations are not subject to cancellation fees. If you registered as a free visitor and are unable to attend, no action is required.
            </p>
        </article>

        <!-- 3. Exhibitor & Stall Booking Cancellation -->
        <article class="policy-section" aria-labelledby="crp-exhibitor">
            <h2 id="crp-exhibitor">Exhibitor &amp; Stall Booking Cancellation</h2>
            <p>
                Exhibition stall bookings represent a significant commitment of venue resources and planning. Accordingly, cancellations for confirmed stall bookings are subject to the following schedule, calculated from the date EGN ConnectX receives written notice of cancellation:
            </p>
            <ul>
                <li><strong>More than 120 days before the event:</strong> 60% refund of the total booking amount paid. An administration and slot-reservation fee of 40% is non-refundable.</li>
                <li><strong>Between 90 and 120 days before the event:</strong> 40% refund of the total booking amount paid.</li>
                <li><strong>Between 60 and 90 days before the event:</strong> 20% refund of the total booking amount paid.</li>
                <li><strong>Less than 60 days before the event:</strong> No refund. Full booking amount is forfeited.</li>
                <li><strong>Post-setup or on-site cancellation:</strong> No refund or compensation of any kind will be provided.</li>
            </ul>
            <div class="policy-note" role="note">
                All stall cancellations must be submitted in writing to <a href="mailto:enquiry@egnindia.com">enquiry@egnindia.com</a>. Verbal or informal cancellations will not be accepted. The cancellation date is the date on which written confirmation is received by EGN ConnectX.
            </div>
            <p>
                EGN ConnectX reserves the right to reallocate a cancelled stall space to another exhibitor immediately upon receipt of a cancellation notice. No preferential rights over the cancelled space are retained by the cancelling party.
            </p>
        </article>

        <!-- 4. Sponsorship Cancellation Policy -->
        <article class="policy-section" aria-labelledby="crp-sponsorship">
            <h2 id="crp-sponsorship">Sponsorship Cancellation Policy</h2>
            <p>
                Sponsorship packages involve advance procurement of event deliverables, marketing materials, branding placements, and third-party production commitments. The following terms apply to sponsorship cancellations:
            </p>
            <ul>
                <li><strong>More than 120 days before the event:</strong> 50% refund of the sponsorship amount paid, provided no creative, production, or branding work has been initiated.</li>
                <li><strong>Between 90 and 120 days before the event:</strong> 30% refund of the sponsorship amount paid, net of any costs already incurred by EGN ConnectX on the sponsor's behalf.</li>
                <li><strong>Between 60 and 90 days before the event:</strong> 15% refund of the sponsorship amount paid, subject to cost deductions.</li>
                <li><strong>Less than 60 days before the event:</strong> No refund. The full sponsorship amount is forfeited, as commitments made to third-party vendors and production partners cannot be reversed at this stage.</li>
            </ul>
            <p>
                Where creative work, branding assets, digital placements, or print materials have already been produced at the time of cancellation, the actual cost of such work will be deducted from any eligible refund amount before processing.
            </p>
            <p>
                Sponsorship packages are non-transferable to other individuals, organisations, or events unless expressly agreed in writing by EGN ConnectX.
            </p>
        </article>

        <!-- 5. Refund Processing Timelines -->
        <article class="policy-section" aria-labelledby="crp-timelines">
            <h2 id="crp-timelines">Refund Processing Timelines</h2>
            <p>
                All approved refunds will be processed in accordance with the following timelines:
            </p>
            <ul>
                <li>Refund requests are reviewed and approved within <strong>7–10 business days</strong> of receiving a written cancellation request with all required details.</li>
                <li>Once approved, refunds are credited to the original payment method within <strong>15–30 business days</strong>, depending on the payment gateway and banking institution involved.</li>
                <li>Refunds are processed in Indian Rupees (INR) only. EGN ConnectX is not responsible for any currency conversion charges, banking fees, or exchange rate differences that may apply for international transactions.</li>
                <li>Credit notes, where issued in lieu of cash refunds, will be valid for use at the next available EGN ConnectX event edition and will be communicated in writing within 10 business days of the approved cancellation.</li>
            </ul>
            <p>
                To initiate a refund, please contact us at <a href="mailto:enquiry@egnindia.com">enquiry@egnindia.com</a> with your booking reference number, registered name, organisation, and reason for cancellation.
            </p>
        </article>

        <!-- 6. Event Rescheduling & Cancellation by Organiser -->
        <article class="policy-section" aria-labelledby="crp-organiser">
            <h2 id="crp-organiser">Event Rescheduling &amp; Cancellation by Organiser</h2>
            <p>
                EGN ConnectX reserves the right to reschedule, postpone, or cancel the event at any time due to circumstances within or beyond its control. In the event of an organiser-initiated change:
            </p>
            <ul>
                <li><strong>Event Rescheduling:</strong> If the event is postponed to a new date within the same calendar year or up to 12 months from the original date, all confirmed bookings will be automatically transferred to the new event date. Participants who are unable to attend the rescheduled date may request a full refund within 14 days of the rescheduling announcement.</li>
                <li><strong>Event Cancellation by EGN ConnectX:</strong> In the unlikely event of a full event cancellation by the organiser (other than due to force majeure), all registered paid participants will receive a full refund of the amounts paid, processed within 30 business days of the cancellation announcement.</li>
                <li><strong>Partial Event Changes:</strong> Modifications to specific sessions, speakers, schedules, or programme formats do not constitute event cancellation and do not entitle participants to a refund.</li>
            </ul>
            <div class="policy-note" role="note">
                EGN ConnectX will communicate all rescheduling or cancellation decisions via registered email addresses and official website announcements. Participants are responsible for ensuring their registered email address is accurate and active.
            </div>
        </article>

        <!-- 7. Force Majeure -->
        <article class="policy-section" aria-labelledby="crp-force">
            <h2 id="crp-force">Force Majeure</h2>
            <p>
                EGN ConnectX shall not be liable for any failure, delay, modification, or cancellation of the event caused directly or indirectly by circumstances beyond its reasonable control ("Force Majeure Events"), including but not limited to:
            </p>
            <ul>
                <li>Natural disasters, earthquakes, floods, cyclones, or other acts of God</li>
                <li>Pandemics, epidemics, outbreaks, or public health emergencies declared by government or international health authorities</li>
                <li>Government orders, regulatory directives, travel bans, or venue access restrictions</li>
                <li>Acts of war, terrorism, civil unrest, riots, or armed conflict</li>
                <li>Strikes, industrial actions, or venue-level closures not attributable to EGN ConnectX</li>
                <li>Critical infrastructure failures including power outages, internet blackouts, or transport disruptions affecting the event</li>
            </ul>
            <p>
                In a Force Majeure Event, EGN ConnectX will make all reasonable efforts to reschedule the event and provide advance notice to all registered participants. Where rescheduling is not possible within a reasonable period, EGN ConnectX will offer affected participants either a credit note for the next event edition or a partial refund, after deducting non-recoverable costs already incurred by the organiser.
            </p>
            <p>
                EGN ConnectX shall not be held liable for travel, accommodation, visa, or any other ancillary costs incurred by participants in connection with the event, irrespective of the reason for any cancellation or rescheduling.
            </p>
        </article>

        <!-- 8. Non-Refundable Situations -->
        <article class="policy-section" aria-labelledby="crp-nonrefund">
            <h2 id="crp-nonrefund">Non-Refundable Situations</h2>
            <p>
                The following situations will not qualify for any refund, credit note, or compensation under any circumstances:
            </p>
            <ul>
                <li>Failure to attend the event without prior written cancellation (no-show)</li>
                <li>Late arrival at the event resulting in missed sessions, workshops, or programmes</li>
                <li>Cancellation requests submitted after the applicable deadline for the booking category</li>
                <li>Dissatisfaction with individual speakers, sessions, panellists, or programme content</li>
                <li>Personal reasons including travel disruptions, health issues, or schedule conflicts, unless falling under a government-declared public health emergency</li>
                <li>Loss or misuse of event access credentials, badges, or registration confirmation</li>
                <li>Removal from the event premises due to violation of the Code of Conduct or these Terms</li>
                <li>Bookings made in error, including duplicate registrations, where EGN ConnectX was not notified within 48 hours of the booking confirmation</li>
                <li>Requests for refund of GST or other applicable taxes once an invoice has been issued</li>
            </ul>
        </article>

        <!-- 9. Transfer & Substitution Policy -->
        <article class="policy-section" aria-labelledby="crp-transfer">
            <h2 id="crp-transfer">Transfer &amp; Substitution Policy</h2>
            <p>
                EGN ConnectX understands that circumstances may change after a booking is confirmed. As an alternative to cancellation, the following transfer and substitution options are available:
            </p>
            <ul>
                <li><strong>Delegate Substitution:</strong> A registered paid delegate may transfer their participation to a colleague within the same organisation at no additional charge, provided written notice is given at least <strong>14 days before the event</strong>. Substitution requests submitted with less than 14 days' notice may be subject to an administrative fee of ₹500.</li>
                <li><strong>Stall Booking Transfer:</strong> A confirmed exhibition stall booking may be transferred to another organisation only with the prior written approval of EGN ConnectX. Unapproved transfers will be treated as cancellations and subject to the cancellation schedule above.</li>
                <li><strong>Sponsorship Package Transfer:</strong> Sponsorship packages are not transferable to other organisations or events without express written consent from EGN ConnectX.</li>
            </ul>
            <p>
                All substitution and transfer requests must be submitted in writing to <a href="mailto:enquiry@egnindia.com">enquiry@egnindia.com</a> with full details of both the original registrant and the proposed substitute or transferee.
            </p>
        </article>

        <!-- 10. Payment & Tax Terms -->
        <article class="policy-section" aria-labelledby="crp-payment">
            <h2 id="crp-payment">Payment &amp; Tax Terms</h2>
